
public class Q5Arithmetic_operations {
	public static void main(String arg[])
	{
		int a=10,b=20;
		System.out.println("Sum A + B = " + (a+b));
		System.out.println("Sub A - B = " + (a-b));
		System.out.println("Mul A * B = " + (a*b));
		System.out.println("Sum A / B = " + (a/b));
		System.out.println("Sub A % B = " + (a%b));
	}

}
